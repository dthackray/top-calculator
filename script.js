function add (a, b) {
    return a + b
}

function subtract (a, b) {
    return a - b
}

function multiply (a, b) {
    return a * b
}

function divide (a, b) {
    return a / b
}

console.log(add(2, 2))
console.log(subtract(10, 3))
console.log(multiply(2, 4))
console.log(divide(50, 5))